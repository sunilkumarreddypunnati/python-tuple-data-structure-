'''Task 2: Indexing & Slicing

Problem:
Given a tuple: colors = ("red", "green", "blue", "yellow", "pink")
Print the first and last colors
Print a slice containing "green", "blue", "yellow"

Output:
red
pink
('green', 'blue', 'yellow')'''

colors = ("red", "green", "blue", "yellow", "pink")
print(colors[0])
print(colors[-1])
print(colors[1:4])